public class swapTwoVariables {
  public static void main(String[] args) {
  int x = 120;
  int y = 100;
  int aux = 0;
  aux = x;
  x = y;
  y = aux;
  System.out.println(x);
  System.out.println(y);
  String juice = "kool-Aid";
  String water = "Water";
  String glass = "";
  glass = juice;
  juice = water;
  water = glass;

  System.out.println(juice);
  System.out.println(water);
  }
}
